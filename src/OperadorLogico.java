public class OperadorLogico {
    public static void main(String[] args) {
        System.out.println(true);
        System.out.println(1 == 0);
        System.out.println(5 > 2);
        System.out.println(2 >= 2);
        System.out.println(1 <= 1);
        System.out.println(1 == 1);
    }
}
